    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
//        processRequest(request, response);
        AccountDAO adb = new AccountDAO();
        List<Account> list1 = adb.getAll();
        
        ProductDAO pdb = new ProductDAO();
        
        
        HttpSession session = request.getSession();
        Account a = (Account) session.getAttribute("account");
        if (a==null) {
            response.sendRedirect("login");
            return;
        }
        
        if (a.getRoleID()==1) {
            List<Product> list2 = pdb.getAll();
            request.setAttribute("acc", list1);
            request.setAttribute("pro", list2);
            request.getRequestDispatcher("dashboard.jsp").forward(request, response);
        } else if (a.getRoleID()==3) {
            List<Product> list3 = pdb.getByAccountID(a.getID());
            request.setAttribute("pro", list3);
            request.getRequestDispatcher("dashboard.jsp").forward(request, response);
        }
    }

